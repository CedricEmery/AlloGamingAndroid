package fr.epsi.emery_legoff_itier.allogaming;

public class Platform {

	protected int IPlatformId;
	protected String sPlatformName;
	
	
	/**
	 * @return the iPlatformId
	 */
	public int getIPlatformId() {
		return IPlatformId;
	}
	
	/**
	 * @param iPlatformId the iPlatformId to set
	 */
	public void setIPlatformId(int iPlatformId) {
		IPlatformId = iPlatformId;
	}
	
	/**
	 * @return the sPlatformName
	 */
	public String getsPlatformName() {
		return sPlatformName;
	}
	
	/**
	 * @param sPlatformName the sPlatformName to set
	 */
	public void setsPlatformName(String sPlatformName) {
		this.sPlatformName = sPlatformName;
	}
}
